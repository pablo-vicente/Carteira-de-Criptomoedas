    //Calcua as taxa e valores da moeda ao trocar no combobox
    private void comboMoedasComprasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboMoedasComprasItemStateChanged
        ControladorCarteira.getIinstance().setTrocaMoedaCampoMontante(true);
        
        int moeda = comboMoedasCompras.getSelectedIndex()+1; //pega indice do combo de moedas
        double montanteCompra = pedirDouble(campoMontanteCompras.getText());//começa com zero caso na não seja digitadi nada evitar erro 
        double[] dadosTrocaMoedas = controladorCarteira.conversorCompraVendaMoeda(moeda,montanteCompra,0,"Compra");//Passa informamoes para trocar de moedas
        String valorMoeda = formatarParaExibir(dadosTrocaMoedas[0]);//valor da meoda trocado em reais
        String cotacao = formatarParaExibir(dadosTrocaMoedas[1]);//valor da cotacao na hora da troca
        String qntTaxa = formatarParaExibir(dadosTrocaMoedas[2]);//quantidade taxa
        String qntLiquida = formatarParaExibir(dadosTrocaMoedas[3]);//quantidade de moedas creditadas

        campoValorMoedaCompradoReais.setText(valorMoeda);//exibe na tele  valor a moeda
        campoCotacaoMoedaCompras.setText(cotacao);//exibe na tela a contacao
        campoTaxaCompras.setText(qntTaxa);//exibe a quantiade taxa
        campoLiquidoCompras.setText(qntLiquida);//exibe a quantiade de moedas que será creditada
        
    }//GEN-LAST:event_comboMoedasComprasItemStateChanged

