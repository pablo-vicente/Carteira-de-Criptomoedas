    public String formatarParaExibir(double valorParaFormatar){
        String valorFormatado ="0";
        DecimalFormat formatter = new DecimalFormat("#,##0.000");
        valorFormatado = formatter.format(valorParaFormatar);
        return valorFormatado;
    }
    
    public double converterValorParaDouble(String campoValor){
        campoValor = campoValor.replace(".", "").replace(",", ".");
        double valorCampo = Double.parseDouble(campoValor);
        return valorCampo;
    }
    
    public void exibirDadosCarteira(double[]quantidades, double[]cotacoes,double[]conversoes){
        carteiraBitcoin.setText(formatarParaExibir(quantidades[0]));
        carteiraLitecoin.setText(formatarParaExibir(quantidades[1]));
        carteiraBcash.setText(formatarParaExibir(quantidades[2]));
        carteiraMonero.setText(formatarParaExibir(quantidades[3]));
        carteiraDogcoin.setText(formatarParaExibir(quantidades[4]));
        carteiraReais.setText(formatarParaExibir(quantidades[5]));
        
        cotacaoBitcoin.setText(formatarParaExibir(cotacoes[0]));
        cotacaoLitecoin.setText(formatarParaExibir(cotacoes[1]));
        cotacaoBcash.setText(formatarParaExibir(cotacoes[2]));
        cotacaoMonero.setText(formatarParaExibir(cotacoes[3]));
        cotacaoDogcoin.setText(formatarParaExibir(cotacoes[4]));
        
        bitcoinConvertido.setText(formatarParaExibir(conversoes[0]));
        litecoinConvertido.setText(formatarParaExibir(conversoes[1]));
        bcashConvertido.setText(formatarParaExibir(conversoes[2]));
        moneroConvertido.setText(formatarParaExibir(conversoes[3]));
        dogecoinConvertido.setText(formatarParaExibir(conversoes[4]));
        
        campoSaldoCompra.setText(formatarParaExibir(quantidades[5])); // Campo Saldo na tela de compras
        
        //Calcula as taxa para valor total da carteira caso usuari queira sacar
        double[] dados = controladorCarteira.calcularTaxas("Saque", converterValorParaDouble(carteiraReais.getText()));
        taxaAdmSaque.setText(formatarParaExibir(dados[0]));
        saldoLiquidoCarteira.setText(formatarParaExibir(dados[1]));
        campoValorSaques.setText(formatarParaExibir(converterValorParaDouble(carteiraReais.getText())));
    }
    
